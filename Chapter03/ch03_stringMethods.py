# 문자열 관련 메서드

weekDays = "Mon, Tue, Wed, Thur, Fri, Sat, Sun"

# split()
print(weekDays.split(", "))

# replace()
print(weekDays.replace("M", "m"))

# upper()
print(weekDays.upper())

# lower()
print(weekDays.lower())

# capitalize()
print(weekDays.capitalize())

# title()
print(weekDays.title())

# startswith()
print(weekDays.startswith("M"))
print(weekDays.startswith("k"))

# endswith()
print(weekDays.endswith("n"))
print(weekDays.endswith("E"))
